# ＜LangGraphとは何か＞
# - LLMを活用した複雑なワークフローを開発するためのPythonライブラリ
# - ワークフローをグラフ構造に設計してモデル化することができる
# - 各パーツをノード、パーツの継ぎ目をエッジという


# ＜グラフ構造アプローチ＞
# - 各ノードが処理や判断を担当し、エッジが処理間のつながりや関係性を表現する
# - 従来のプログラミングが処理順に記述するのに対して、グラフでは条件分岐に応じたワークフローを構築できる
# - グラフ形式で表現することで、人間の直感に合う視覚的な設計が可能となる
# - ノードごとにパーツ化されているため、開発/テストを独立させることができる（開発効率の向上）


# ＜主なメリット＞
# 1. 明示的なステート管理
# 2. 条件分岐やループの自然な表現
# 3. パーツ化による段階的な拡張性
# 4. デバッグとテストのしやすさ
# 5. チェックポイントとリカバリによる保守性
