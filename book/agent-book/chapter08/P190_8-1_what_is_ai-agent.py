# ＜AIエージェントとは＞
# - 特定のタスクを自律的に実行するAIシステム
# - 従来のAIシステムは単一のタスクに特化していたが、AIエージェントは達成のために必要な行動を自ら決定する

# ＜AIエージェントの特徴＞
# - 自律性: タスクを自分で決定し、実行する能力
# - 適応性: 環境の変化に応じて行動を調整する能力
# - 学習能力: 経験から学び、パフォーマンスを向上させる能力

# ＜単独プロンプトの課題＞
# - タスクの分割や複数のステップを必要とする場合、単独プロンプトでは限界がある  
# - 人間の意図に合った出力をするためには、指示を明確にしたプロンプトの作りこみが必要となる
# - 出力結果に対する人間による評価も必要となり、期待しているよりも多くの手間がかかる

# ＜AIエージェントへの期待＞
# - 単独プロンプトの限界を克服し、より複雑なタスクを自律的に実行できる
# - 結果としてプログラム/プロンプトが簡素化されメンテンナンス性が向上する
