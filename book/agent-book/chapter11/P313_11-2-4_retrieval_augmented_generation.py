# ＜概要＞
# - AIエージェントの基礎となるLLMが持つ知識には限界があり、専門分野ではより顕著となる
# - RAGはLLMの生成能力と外部ソースの検索を組み合わせて解決する
# - 検索プロンプトはエージェントプロセスの中で洗練させていく仕組みが入っている
# - 適切な情報源の選択、検索結果の信頼性評価などの課題をクリアする必要がある
# - 実行時間も長くなりがちなので、プロセス整理で応答パフォーマンスの改善も必要となる


# ＜カテゴリ＞
# - 推論の確実性向上


# ＜関連パターン＞
# - 1. パッシブ・ゴールクリエイター
# - 2. プロアクティブ・ゴールクリエイター
# - 3. プロンプト/レスポンス最適化


# ＜プロセス＞
# - 1. ユーザーから質問や要求を受け取る
# - 2. 質問から検索クエリを生成する
# - 3. 生成されたクエリを使用して外部情報ソースから情報を検索する
# - 4. 検索結果をフィルタリング/リランキングして最も関連性の高い情報を選択する
# - 5. 選択された情報と元の質問を組み合わせて、新しいプロンプトを生成する
# - 6. 作成されたプロンプトをLLMに入力して回答を得る
# - 7. 必要に応じて生成された回答の事実確認や補足を行う
